#include "push_swap.h"

void	fill_stack_a(t_data *stack, char **argv)
{
	int	i;

	i = 0;
	while (argv[i + 1])
	{
		stack->a[i] = ft_atoi(argv[i + 1]);
		i++;
	}
}
